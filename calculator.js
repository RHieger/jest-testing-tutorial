const mathOperations = {
  sum: function(a, b) {
    return a + b;
  },
  difference: function(a, b) {
    return a - b;
  },
  product: function(a, b) {
    return a * b;
  },
  quotient: function(a, b) {
    return a / b;
  },
  modulus: function(a, b) {
    return a % b;
  }
};

module.exports = mathOperations;
